void cordic(bool select, FIXDT CS_I_T, FIXDT IA_I_S, FIXDT IA_I_C, FIXDT &CS_O_SIN, FIXDT &CS_O_COS, FIXDT &IA_O_IAT,short &done) {
//#pragma HLS INTERFACE s_axilite port=return bundle=HLS_TREEADD_PERIPH_BUS
//#pragma HLS INTERFACE s_axilite port=select bundle=HLS_TREEADD_PERIPH_BUS
//#pragma HLS INTERFACE s_axilite port=CS_I_T bundle=HLS_TREEADD_PERIPH_BUS
//#pragma HLS INTERFACE s_axilite port=IA_I_S bundle=HLS_TREEADD_PERIPH_BUS
//#pragma HLS INTERFACE s_axilite port=IA_I_C bundle=HLS_TREEADD_PERIPH_BUS
//#pragma HLS INTERFACE s_axilite port=CS_O_SIN bundle=HLS_TREEADD_PERIPH_BUS
//#pragma HLS INTERFACE s_axilite port=CS_O_COS bundle=HLS_TREEADD_PERIPH_BUS
//#pragma HLS INTERFACE s_axilite port=IA_O_IAT bundle=HLS_TREEADD_PERIPH_BUS
//#pragma HLS INTERFACE s_axilite port=done bundle=HLS_TREEADD_PERIPH_BUS

	FIXDT x = 0;
	FIXDT y = 0;
	FIXDT t = 0;
	if (!select) { // select == 0: sin cos mode
		x = 0.60735;
		y = 0;
		t = CS_I_T;
	} else { // select == 1; arc tan mode
		x = IA_I_C;
		y = IA_I_S;
		t = 0;
	}
	for (int itr = 0; itr < NUM_ITERATIONS; itr ++){
//	#pragma HLS PIPELINE
#pragma HLS pipeline off
		int sigma;
		if (!select) { // select == 0: sin cos mode
			sigma = (t < 0) ? -1 : 1;
		} else { // select == 1; arc tan mode
			sigma = (y < 0) ? 1: -1;
		}


		FIXDT x_shift = (x * sigma) >> itr;
		FIXDT y_shift = (y * sigma) >> itr;

		x = x - y_shift;
		y = y + x_shift;

		t = t - cordic_phase[itr] * sigma;
	}
	if (!select) { // select == 0: sin cos mode
		CS_O_SIN = y;
		CS_O_COS = x;
		IA_O_IAT = 0;
	} else { // select == 1; arc tan mode
		CS_O_SIN = 0;
		CS_O_COS = 0;
		IA_O_IAT = t;
	}
	done = 1;
}




